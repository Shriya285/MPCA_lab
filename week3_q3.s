.DATA
A: .WORD 10, -3, 41, -55, 30, 22, 0, 5, 83, 0
POS: .WORD 40
NEG: .WORD 40
ZERO: .WORD 40
.TEXT
LDR R0 ,=A
LDR R1 ,=POS
LDR R2, =NEG
LDR R3, =ZERO
LDR R4,=10

CLASSIFY:
LDR R8, [R0]     
CMP R8,#0
BGT POS_NO
BLT NEG_NO
BEQ ZERO_NO

POS_NO:
STR R8,[R1],#4
B CONTINUE

NEG_NO:
STR R8, [R2],#4
B CONTINUE

ZERO_NO:
STR R8,[R3],#4

CONTINUE:
ADD R0,R0,#4
SUBS R4,R4,#1
BNE CLASSIFY
